public class LogicalOperators {

    public static void main(String[] args) {
        boolean c = true;
        boolean d = false;

        // it based working on truth table

        //Not Operator = !

        System.out.println(!c);
        System.out.println(!d);

        // Or Operator = ||

        System.out.println(c||d);


        // And Operator = &&

        System.out.println(c&&d);

    }

}
