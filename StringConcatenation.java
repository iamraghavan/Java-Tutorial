public class StringConcatenation {
    public static void main(String[] args) {

        String fName = "Raghavan";
        String sName = "Jeeva";

        System.out.println(fName + " " + sName);

        /*  Use Concat() Method to Concatenation the Two or More String  */

        String concatName = fName + " " .concat(sName);
        System.out.println(concatName);


        String x = "20";
        String y = "24";
        System.out.println(x+y);


        String g = "10";
        int h = 20;

        System.out.println(g+h);

    }
}
