public class Arrays {
    public static void main(String[] args) {
        /* Declare an Array */

        int [] rollno = new int[50];

        rollno[0] = 1;
        rollno[2] = 3;

        System.out.println(rollno[0]);
        System.out.println(rollno[1]);

        for (int i = 0 ; i < 5 ; i++){
            System.out.println(rollno[i]);
        }


    }
}
