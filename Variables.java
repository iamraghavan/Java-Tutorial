public class Variables {

    public static void main(String[] args) {

        int firstInt = 10; // Integer ( Whole Number )
        float firstFloat  = 5.93f; // Floating Point Numbers
        char firstCharacter = 'c'; //Character
        boolean firstBoolean = true; // Boolean --> true : 1 , false : 0
        String fistString = "Raghavan"; // String  --- > ""

        System.out.println(firstInt);
        System.out.println(firstFloat);
        System.out.println(firstCharacter);
        System.out.println(firstBoolean);
        System.out.println(fistString);






    }

}
