public class SwitchStatement {

    public static void main(String[] args) {

        String  attendance = "A";

        switch (attendance){

            case "O":
                System.out.println("On - Duty");
                break;
            case "P":
                System.out.println("Attend Class");
                break;
            case "A":
                System.out.println("Not Attend Class");
                break;

            default:
                System.out.println("Invalid");





        }



    }

}
